-- 1693. Distinct leads and partners

-- 1. let's get distinct date
SELECT date_id, COUNT(DISTINCT date_id)
FROM DailySales
GROUP BY date_id

-- 2. let's get distinct name
SELECT make_name, COUNT(DISTINCT make_name)
FROM DailySales
GROUP BY make_name

-- 3. combine group by
SELECT date_id, make_name,
    COUNT(DISTINCT date_id) AS unique_leads,
    COUNT(DISTINCT make_name) AS unique_partners
FROM DailySales
GROUP BY date_id, make_name