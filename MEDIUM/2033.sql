-- Write a query that calculates the average signup duration in days and the average transaction amount for each location. Then, calculate the ratio of average transaction amount to average duration.
-- Your output should include the location, average signup duration (in days), average transaction amount, and the ratio. Sort the results by ratio in descending order.


-- 1. average signup duration
-- 2. average transaction amount
-- 3. calculate ratio - average transaction amount / average signup duration

SELECT s.location,
       (AVG(t.amt) / AVG(s.signup_stop_date - s.signup_start_date)) AS ratio
FROM transactions t
JOIN signups s ON t.signup_id = s.signup_id
GROUP BY 1