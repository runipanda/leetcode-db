-- 2113. Exremely Late Deliveries

WITH late_orders AS (
    SELECT TO_CHAR(order_placed_time, 'YYYY-MM') AS YEAR_MONTH, 
            delivery_id,
            CASE WHEN EXTRACT (minute FROM (order_delivered_time - order_picked_up_time)) > 20 THEN 1
        ELSE 0 END AS extremely_late
    FROM delivery_orders
)

    SELECT year_month,
           SUM(extremely_late)/COUNT(distinct delivery_id)::float * 100 AS PERC_EXTREMELY_LATE
    FROM late_orders        
    GROUP BY year_month